package models;

public class BGBean {

	int red, green, blue;

	public BGBean() {
		this.red = 119;
		this.green = 136;
		this.blue = 153;
	}

	public int getR() {
		return red;
	}

	public void setR(int r) {
		this.red = r;
	}

	public int getG() {
		return green;
	}

	public void setG(int g) {
		this.green = g;
	}

	public int getB() {
		return blue;
	}

	public void setB(int b) {
		this.blue = b;
	}
}
